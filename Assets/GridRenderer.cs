    void Start()
    {
        housePosition = new Vector3Int(Random.Range(0, southAmericaShape.GetLength(1)), Random.Range(0, southAmericaShape.GetLength(0)), 0);
        tilemap = GetComponent<Tilemap>();
        if (tilemap == null) throw new System.Exception("Could not find Tilemap component on this gameobject");
        RenderHouse(housePosition.x, housePosition.y);
    }

    private void Update()
    {
        var tiles = gameController.GetAllTiles();
        if (tiles == null)
        {
            Debug.LogError("Tiles array is null. Ensure gameController.GetAllTiles() returns a valid array.");
            return;
        }
        RenderGrip(tiles);
    }

    // Function to create a grid with the shape of South America using the provided tile matrix
    void RenderGrip(TileModel[,] tiles)
    {
        for (int y = 0; y < southAmericaShape.GetLength(0); y++)
        {
            for (int x = 0; x < southAmericaShape.GetLength(1); x++)
            {
                if (southAmericaShape[y, x] == 0) continue;

                // Ensure x and y are within the bounds of the tiles array
                if (x >= tiles.GetLength(0) || y >= tiles.GetLength(1))
                {
                    Debug.LogWarning($"Index ({x}, {y}) is out of bounds for tiles array. Skipping.");
                    continue;
                }

                TileModel tile = tiles[x, y];
                if (tile == null)
                {
                    Debug.LogWarning($"Tile at ({x}, {y}) is null. Skipping.");
                    continue;
                }

                Vector3Int gridPos = new Vector3Int(x, y, 0);
                if (x == housePosition.x && y == housePosition.y) continue;

                tile.tileType = (TileType)treeGamemode;

                switch (tile.tileType)
                {
                    case TileType.Normal:
                        tile.burnTime = 3;
                        tile.recoveryTime = 3;
                        tile.spreadChancePerSecond = 0.3f;
                        break;
                    case TileType.Jungle:
                        tile.burnTime = 5;
                        tile.recoveryTime = 9;
                        tile.spreadChancePerSecond = 0.2f;
                        break;
                }

                tilemap.SetTile(gridPos, ChooseTileForState(tile));  // Set the tile at this position to the appropriate tile
            }
        }
    }

    public static void ChangeGamemode(TreeGamemode newGamemode)
    {
        treeGamemode = newGamemode;
    }

    private void RenderHouse(int x, int y)
    {
        TileModel house = new TileModel();
        house.tileType = TileType.House;
        Debug.Log("House at " + x + ", " + y);
        tilemap.SetTile(new Vector3Int(x, y, 0), ChooseTileForState(house));
    }

    Tile ChooseTileForState(TileModel tile)
    {
        switch (tile.getState())
        {
            case TileState.Healthy:
                if (tile.tileType == TileType.Normal) return sprites_Healthy;
                else if (tile.tileType == TileType.Jungle) return sprites_Jungle;
                else if (tile.tileType == TileType.House) return sprites_House;
                else return null;
            case TileState.Burning:
                return sprites_Burning;
            case TileState.Dead:
                return sprites_Dead;
            default:
                return null;
        }
    }
}

public enum TreeGamemode
{
    Normal,
    Jungle,
